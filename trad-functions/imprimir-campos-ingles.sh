imprimir-campos-ingles()
{
    echo -e "$BLANCO=================================="
    echo -e "$VERDE $INGLES : $ROSA $ESPANOL"
    echo -e "$BLANCO==================================" 
    echo -e "$BLANCO Extra: $VERDE $EXTRA"
    echo -e "$BLANCO Past: $VERDE $P" 
    echo -e "$BLANCO Past Participe: $VERDE $PP"
    echo -e "$BLANCO Gerund: $VERDE $G"
    echo -e "$BLANCO Ejemplo: $E"
}
